/// The raw tonic output for `coppice.raft.v1`: one client and one server
/// module per service, generated by `build.rs`. Message types resolve to
/// [`coppice_proto::pb::raft::v1`] via `extern_path`, so nothing here
/// redefines a schema type.
pub mod pb {
    include!(concat!(env!("OUT_DIR"), "/coppice.raft.v1.rs"));
}

